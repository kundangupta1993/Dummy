                        <h1 class="post-title">ThinkSys Announces Cal Hacks Sponsorship, First Major Collegiate
                            Hackathon In The San
                            Francisco Bay Area</h1>
                        <div class="post-meta">
                            <span class="author">By <a href="#">Thinksys</a></span><br><br>
                            <div><time class="pub-date" datetime="2020-06-24">Sunnyvale, CA, September 26, 2014</time>
                                <div>
                                </div>
