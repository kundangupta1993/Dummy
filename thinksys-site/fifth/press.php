                            <div class="post-content">
                                <p>ThinkSys Inc, a global technology company focused on software development,
                                    e-commerce, QA,
                                    and QA automation services, is proud to announce its sponsorship of Cal Hacks, the
                                    first
                                    major collegiate hackathon in the San Francisco Bay Area.</p>
                                <p>ThinkSys is a longtime advocate of the hackathon concept,” says Leslie Sarandah, Vice
                                    President of Sales and Marketing. “Our executives have championed hackathons as a
                                    way to
                                    encourage motivated teams of developers to break away from their day-to-day
                                    responsibilities
                                    and work in teams on projects of their own design. This type of activity generates
                                    some
                                    amazing customer-centric innovations in a very short period of time.”</p>
                                <p>Alexander Kern, Director of Cal Hacks states “The hackathon attracts natural problem
                                    solvers.
                                    We expect this event to bring together some of the brightest students in their
                                    fields to
                                    direct their energies toward complex problems, and with their solutions ultimately
                                    being
                                    judged by leaders in the technology industry. We are really excited to see the
                                    results.”</p>
                                <p>Cal Hacks will take place October 3 – 5 at U. C. Berkeley’s Cal Memorial Stadium. The
                                    event
                                    will bring together hundreds of undergraduate innovators, coders, and hackers from
                                    around
                                    the world to create incredible software and hardware projects. This collaborative
                                    experience
                                    offers invaluable connections, mentorship and teambuilding that will benefit
                                    participants
                                    today and in the future. The event will last 36 hours and is free to accepted
                                    participants.
                                </p>
                                <h3>About ThinkSys Inc</h3>
                                <p>ThinkSys, a global technology products & services company, helps customers improve
                                    and grow
                                    their business and e-commerce initiatives across the Web and mobile channels.
                                    Employing over
                                    120 technology specialists, ThinkSys develops, tests and implements high-quality,
                                    cost-effective solutions running in the cloud or on premise. ThinkSys is privately
                                    held and
                                    is headquartered in Sunnyvale, CA. For more information visit ThinkSys.com</p>
                                <h3>About Cal Hacks</h3>
                                <p>Cal Hacks is the first major collegiate hackathon to take place in the San Francisco
                                    Bay
                                    Area. Additional sponsors of the event include Microsoft, Google, Dropbox and
                                    Facebook. For
                                    more information and a complete list of sponsors, go to <a
                                        href="http://www.calhacks.io">calhacks.io</a></p>
