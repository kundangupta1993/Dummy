                <a class="card-link" data-toggle="collapse" href="#ecom-2">
                  How to do API Testing?
                </a>
              </div>
              <div id="ecom-2" class="collapse" data-parent="#accordion1">
                <div class="card-body">
                  <p>The process of API testing involves the following steps:</p>
                  <ul class="list-style">
                    <li><strong>Discovery Testing:</strong> The calls listed in the API documentation are executed by
                      the tester to validate whether it can be enumerated, updated, and deleted.</li>
                    <li><strong>Usability Testing:</strong> Helps verify the functionality and user-friendliness of the
                      API, as well as whether it integrates well with other platforms. </li>
                    <li><strong>Security Testing:</strong> Helps validate the type of authentication required as well as
                      whether the sensitive data is encrypted over HTTP. </li>
                    <li><strong>Automated Testing:</strong> Performed to execute the API in timely intervals.</li>
                    <li><strong>Documentation:</strong> Finally, documents and reports with sufficient details about the
                      API and the ways to interact with it are provided by the team along with other final deliverables.
                    </li>
                  </ul>
                </div>
              </div>
